// Префиксный вариант ++i:

let i = 0;
while (++i < 5) alert(i);

// Постфиксный вариант i++

let j = 0;
while (j++ < 5) alert(j);

// Первом цикл выведет от 1 до 4, второй от 1 до 5, т. к. в первом случае сначала увеличивается значение,
// а потом идет сравнение.
// Во втором случае сначала сравнение идет со старым значением
